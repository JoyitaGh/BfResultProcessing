    private boolean isnumeric(String s){
        try{
            float f = Float.parseFloat(s);
            if(f>0.0 && f<=10.0){
             
                return true;
            }
            else if(f==0.0){
                  int a = JOptionPane.showConfirmDialog(null,"Warning:Student may have been absent"
                          + " for the section in which entered mark is 0 or the mark entered is incorrect.","Select",JOptionPane.OK_CANCEL_OPTION,0);  
                if(a==JOptionPane.OK_OPTION){
                    return true;
                }
                else{
                  return false;  
                }
                }
            else{
                JOptionPane.showMessageDialog(this,"Please enter a valid value"); 
                return false;
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,""+e);
        }
        return false;
    }
