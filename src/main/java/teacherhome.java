    public boolean isemptyteacher()
    {
        if(jTextField34.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Name is missing");
            return false;
        }
         if(jTextField35.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Designation is missing");
            return false;
        }
          if(jTextField36.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"E-mail is missing");
            return false;
        }
            if(!jTextField36.getText().matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this,"Invalid e-mail");
            return false;
        }
         
           
            if(jTextField37.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Phone number is missing");
            return false;
        }
            if(String.valueOf(jPasswordField1.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(this,"Password is missing");
            return false;
        }
             return true;
            
             }
    
    
    
