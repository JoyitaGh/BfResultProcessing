    public boolean isemptystudent()
    {
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Session is missing");
            return false;
        }
         if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Student ID is missing");
            return false;
        }
          if(jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Name is missing");
            return false;
        }
           if(jDateChooser1.getDate()== null){
            JOptionPane.showMessageDialog(this,"Date of birth is missing");
            return false;
        }
            if(jDateChooser1.getDate().compareTo(new Date())>0){
            JOptionPane.showMessageDialog(this,"No student from the future are allowed");
            return false;
        }
           
            if(jTextField5.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"E-mail is missing");
            return false;
        }
             if(!jTextField5.getText().matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this,"Invalid e-mail");
            return false;
        }
             if(jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Phone number is missing");
            return false;
        }
              if(jTextField6.getText().length()>=15){
            JOptionPane.showMessageDialog(this,"Phone number is too long");
            return false;
        }
              if(jTextField8.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Address is missing");
            return false;
        }
              return true;
    }
    
